void run() {
  int n;
  cin >> n;
  vec<int> a(n), b(n);
  for (int i = 0; i < n; i++) {
    cin >> a[i];
  }
  vec<vec<int>> g(n);
  for (int i = 0; i < n; i++) {
    cin >> b[i];
    if (b[i] != -1) {
      b[i]--;
      g[b[i]].push_back(i);
    }
  }
  int ans = 0;
  vec<int> v_ans, late_ans;
  function<int(int)> dfs = [&](int v) {
    for (auto u : g[v]) {
      int to_add = dfs(u);
      if (to_add > 0) {
        a[v] += to_add;
      }
    }
    if (a[v] > 0) {
      v_ans.push_back(v + 1);
      ans += a[v];
    } else {
      late_ans.push_back(v + 1);
      ans += a[v];
    }
    return a[v];
  };
  for (int i = 0; i < n; i++) {
    if (b[i] == -1) dfs(i);
  }
  cout << ans << '\n';
  for (auto el : v_ans) {
    cout << el << ' ';
  }
  reverse(ALL(late_ans));
  for (auto el : late_ans) {
    cout << el << ' ';
  }
  cout << '\n';
}

