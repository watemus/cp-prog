void run() {
  int n, m;
  cin >> n >> m;
  vec<int> p(n);
  for (int i = 0; i < n; i++) {
    cin >> p[i];
  }
  vec<int> h(n);
  for (int i = 0; i < n; i++) {
    cin >> h[i];
  }
  vec<vec<int>> g(n);
  for (int i = 0; i < n - 1; i++) {
    int u, v;
    cin >> u >> v;
    u--, v--;
    g[u].push_back(v);
    g[v].push_back(u);
  }
  vec<int> cnt_was(n);
  function<void(int, int)> calc_cnt_was = [&](int v, int pr) {
    cnt_was[v] = p[v];
    for (auto u : g[v]) {
      if (u != pr) {
        calc_cnt_was(u, v);
        cnt_was[v] += cnt_was[u];
      }
    }
  };
  calc_cnt_was(0, 0);
  vec<int> cnt_good(n), cnt_bad(n);
  function<bool(int, int)> check = [&](int v, int pr) {
    int th = 0;
    if ((h[v] + cnt_was[v]) % 2)
      return false;
    cnt_good[v] = (h[v] + cnt_was[v]) / 2;
    cnt_bad[v] = cnt_was[v] - cnt_good[v];
    int cg, cb;
    cg = cb = 0;
    for (auto u : g[v]) {
      if (u != pr) {
        th += h[u];
        bool checked = check(u, v);
        cg += cnt_good[u];
        cb += cnt_bad[u];
        if (!checked)
          return false;
      }
    }
    return abs(h[v]) <= cnt_was[v] && cg + cb + p[v] == cnt_was[v] && cg <= cnt_good[v] && cb + p[v] >= cnt_bad[v];
  };
  cout << (check(0, 0) ? "YES\n" : "NO\n");
}

