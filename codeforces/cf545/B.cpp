vec<int> pi(const string &s) {
  vec<int> p(s.size());
  for (int i = 1; i < s.size(); i++) {
    int cur = p[i - 1];
    while (cur > 0 && s[i] != s[cur]) {
      cur = p[cur - 1];
    }
    if (s[cur] == s[i]) cur++;
    p[i] = cur;
  }
  return p;
}

void run() {
  string s, t;
  cin >> s >> t;
  auto p = pi(t);
  array<int, 2> s_cnt{0, 0}, t_cnt_pref{0, 0}, t_cnt_suf{0, 0};
  for (auto ch : s) {
    s_cnt[ch - '0']++;
  }
  for (int i = 0; i < p.back(); i++) {
    t_cnt_pref[t[i] - '0']++;
  }
  for (int i = p.back(); i < t.size(); i++) {
    t_cnt_suf[t[i] - '0']++;
  }
  int ans = 0;
  string s_ans;
  if (t_cnt_pref[0] <= s_cnt[0] && t_cnt_pref[1] <= s_cnt[1]) {
    for (int i : {0, 1}) {
      s_cnt[i] -= t_cnt_pref[i];
    }
    for (int i = 0; i < p.back(); i++) {
      s_ans.push_back(t[i]);
    }
    while (t_cnt_suf[0] <= s_cnt[0] && t_cnt_suf[1] <= s_cnt[1]) {
      for (int i : {0, 1}) {
        s_cnt[i] -= t_cnt_suf[i];
      }
      for (int i = p.back(); i < t.size(); i++) {
        s_ans.push_back(t[i]);
      }
      ans++;
    }
    for (int i = 0; i < s_cnt[0]; i++) {
      s_ans.push_back('0');
    }
    for (int i = 0; i < s_cnt[1]; i++) {
      s_ans.push_back('1');
    }
    //cout << ans << '\n';
    cout << s_ans << '\n';
  } else {
    //cout << "0\n";
    cout << s << '\n';
  }

}

