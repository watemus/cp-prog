class Segment_tree {
public:
  explicit Segment_tree(int n)
  : n(n), mx(n * 4), agn(n * 4)
  {}
  int get() {
    return mx[0];
  }
  void modify(int lq, int rq, int val) {
    _modify(0, 0, n, lq, rq, val);
  }
private:
  vec<int> mx, agn;
  int n;
  inline void _push(int v, int lb, int rb) {
    mx[v] += agn[v];
    if (rb - lb > 1) {
      agn[v * 2 + 1] += agn[v];
      agn[v * 2 + 2] += agn[v];
    }
    agn[v] = 0;
  }
  void _modify(int v, int lb, int rb, int lq, int rq, int val) {
    _push(v, lb, rb);
    if (lq <= lb && rb <= rq) {
      agn[v] += val;
    } else {
      int mid = (lb + rb) / 2;
      if (lq < mid)
        _modify(v * 2 + 1, lb, mid, lq, rq, val);
      if (mid < rq)
        _modify(v * 2 + 2, mid, rb, lq, rq, val);
      mx[v] = max(mx[v * 2 + 1] + agn[v * 2 + 1], mx[v * 2 + 2] + agn[v * 2 + 2]);
    }
    _push(v, lb, rb);
  }
};

void run() {
  int n;
  cin >> n;
  vec<int> p(n), rev_p(n);
  for (int i = 0; i < n; i++) {
    cin >> p[i];
    rev_p[p[i] - 1] = i;
  }
  Segment_tree t(n);
  vec<int> ans(n);
  ans[0] = n;
  vec<int> q(n);
  for (int i = 0; i < n; i++) {
    cin >> q[i];
    q[i]--;
  }
  int x = n - 1;
  t.modify(0, rev_p[x] + 1, 1);
  for (int i = 0; i < n - 1; i++) {
    t.modify(0, q[i] + 1, -1);
    while (x > 0 && t.get() <= 0) {
      x--;
      t.modify(0, rev_p[x] + 1, 1);
    }
    ans[i + 1] = x + 1;
  }
  for (int i = 0; i < n; i++) {
    cout << ans[i] << ' ';
  }
  cout << '\n';
}

